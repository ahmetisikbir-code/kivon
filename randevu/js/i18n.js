let currentLang = localStorage.getItem('kivon_lang') || 'tr';

export function t(key) {
  return lang[currentLang]?.[key] || lang['tr']?.[key] || key;
}

export function setLanguage(locale) {
  if (lang[locale]) {
    currentLang = locale;
    localStorage.setItem('kivon_lang', locale);
    document.documentElement.lang = locale;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder;
      if (key) el.placeholder = t(key);
    });
  }
}

export function getCurrentLang() {
  return currentLang;
}

export function initI18n() {
  setLanguage(currentLang);
  document.querySelectorAll('.lang-switcher').forEach(el => {
    el.value = currentLang;
    el.addEventListener('change', (e) => {
      setLanguage(e.target.value);
      location.reload();
    });
  });
}
